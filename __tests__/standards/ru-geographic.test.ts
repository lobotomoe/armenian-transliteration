/**
 * Russian Geographic Transliteration (Кузьмина–Туманян 1974) — comprehensive tests.
 *
 * Source: Инструкция по русской передаче географических названий Армянской ССР
 * / Сост. Г. Г. Кузьмина; Ред. Э. Г. Туманян. — М., 1974.
 *
 * Key differences from the former russian-phonetic standard:
 *   - ջ [dʒ] → дж (not ж — matches both Кузьмина and Вартапетян for Eastern Armenian)
 *   - ղ [ʁ]  → к word-initial; г mid-word  (per note 5: "В начале слова — к")
 *   - հ [h]  → х  (kept in geographic names: Хaяcтан, Хачкар)
 *   - ո      → во word-initial + consonant; о elsewhere
 */
import { transliterate } from "../../src";

const t = (text: string) => transliterate(text, { standard: "ru-geographic" });

const B = "\u0562"; // բ — neutral mid-word wrapper
const mid = (ch: string) => `${B}${ch}${B}`;

describe("ru-geographic standard", () => {
  // ─────────────────────────────────────────────────────────────────────────
  // All 38 lowercase letters in mid-word context
  // ─────────────────────────────────────────────────────────────────────────
  describe("38 lowercase letter mappings (mid-word context)", () => {
    test.each([
      ["\u0561", "\u0430"], // ա → а
      ["\u0562", "\u0431"], // բ → б
      ["\u0563", "\u0433"], // գ → г
      ["\u0564", "\u0434"], // դ → д
      ["\u0565", "\u0435"], // ե → е (mid-word)
      ["\u0566", "\u0437"], // զ → з
      ["\u0567", "\u044D"], // է → э
      ["\u0568", "\u044B"], // ը → ы
      ["\u0569", "\u0442"], // թ → т
      ["\u056A", "\u0436"], // ժ → ж
      ["\u056B", "\u0438"], // ի → и
      ["\u056C", "\u043B"], // լ → л
      ["\u056D", "\u0445"], // խ → х
      ["\u056E", "\u0446"], // ծ → ц
      ["\u056F", "\u043A"], // կ → к
      ["\u0570", "\u0445"], // հ → х (geographic: kept as х)
      ["\u0571", "\u0434\u0437"], // ձ → дз
      ["\u0572", "\u0433"], // ղ → г (mid-word)
      ["\u0573", "\u0447"], // ճ → ч
      ["\u0574", "\u043C"], // մ → м
      ["\u0575", "\u0439"], // յ → й
      ["\u0576", "\u043D"], // ն → н
      ["\u0577", "\u0448"], // շ → ш
      ["\u0578", "\u043E"], // ո → о (mid-word; no во)
      ["\u0579", "\u0447"], // չ → ч
      ["\u057A", "\u043F"], // պ → п
      ["\u057B", "\u0434\u0436"], // ջ → дж (KEY: not ж)
      ["\u057C", "\u0440"], // ռ → р
      ["\u057D", "\u0441"], // ս → с
      ["\u057E", "\u0432"], // վ → в
      ["\u057F", "\u0442"], // տ → т
      ["\u0580", "\u0440"], // ր → р
      ["\u0581", "\u0446"], // ց → ц
      ["\u0582", "\u0443"], // ւ → у
      ["\u0583", "\u043F"], // փ → п
      ["\u0584", "\u043A"], // ք → к
      ["\u0585", "\u043E"], // օ → о
      ["\u0586", "\u0444"], // ֆ → ф
    ])("\u0562%s\u0562 → \u0431%s\u0431", (ch, expected) => {
      expect(t(mid(ch))).toBe(`\u0431${expected}\u0431`);
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // All 38 uppercase single-letter words (isolated position → word-initial rules fire)
  // ─────────────────────────────────────────────────────────────────────────
  describe("38 uppercase single-letter words", () => {
    test.each([
      ["\u0531", "\u0410"], // Ա → А
      ["\u0532", "\u0411"], // Բ → Б
      ["\u0533", "\u0413"], // Գ → Г
      ["\u0534", "\u0414"], // Դ → Д
      ["\u0535", "\u0415"], // Ե — word-initial е → Е
      ["\u0536", "\u0417"], // Զ → З
      ["\u0537", "\u042D"], // Է → Э
      ["\u0538", "\u042B"], // Ը → Ы
      ["\u0539", "\u0422"], // Թ → Т
      ["\u053A", "\u0416"], // Ժ → Ж
      ["\u053B", "\u0418"], // Ի → И
      ["\u053C", "\u041B"], // Լ → Л
      ["\u053D", "\u0425"], // Խ → Х
      ["\u053E", "\u0426"], // Ծ → Ц
      ["\u053F", "\u041A"], // Կ → К
      ["\u0540", "\u0425"], // Հ → Х (geographic keeps it)
      ["\u0541", "\u0414\u0417"], // Ձ → ДЗ
      ["\u0542", "\u041A"], // Ղ isolated → К (word-initial rule)
      ["\u0543", "\u0427"], // Ճ → Ч
      ["\u0544", "\u041C"], // Մ → М
      ["\u0545", "\u0419"], // Յ → Й
      ["\u0546", "\u041D"], // Ն → Н
      ["\u0547", "\u0428"], // Շ → Ш
      ["\u0548", "\u0412\u041E"], // Ո isolated → ВО (word-initial + nothing follows)
      ["\u0549", "\u0427"], // Չ → Ч
      ["\u054A", "\u041F"], // Պ → П
      ["\u054B", "\u0414\u0416"], // Ջ → ДЖ (KEY: not Ж)
      ["\u054C", "\u0420"], // Ռ → Р
      ["\u054D", "\u0421"], // Ս → С
      ["\u054E", "\u0412"], // Վ → В
      ["\u054F", "\u0422"], // Տ → Т
      ["\u0550", "\u0420"], // Ր → Р
      ["\u0551", "\u0426"], // Ց → Ц
      ["\u0552", "\u0423"], // Ւ → У
      ["\u0553", "\u041F"], // Փ → П
      ["\u0554", "\u041A"], // Ք → К
      ["\u0555", "\u041E"], // Օ → О
      ["\u0556", "\u0424"], // Ֆ → Ф
    ])("%s → %s", (armenian, expected) => {
      expect(t(armenian)).toBe(expected);
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // ե (U+0565) word-initial → е (same as mid-word in Russian)
  // ─────────────────────────────────────────────────────────────────────────
  describe("ե word-initial context (е, same as mid-word)", () => {
    test("ե isolated → е", () => {
      expect(t("\u0565")).toBe("\u0435");
    });

    test("Ե isolated → Е", () => {
      expect(t("\u0535")).toBe("\u0415");
    });

    test("mid-word ե: բ+ե+բ → бeб", () => {
      expect(t(mid("\u0565"))).toBe("\u0431\u0435\u0431");
    });

    test("Ереван: Ե+ռ+և+ա+ն → Ереван", () => {
      expect(t("\u0535\u057C\u0587\u0561\u0576")).toBe(
        "\u0415\u0440\u0435\u0432\u0430\u043D",
      );
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // ո word-initial: + consonant → во; + vowel/վ → о
  // ─────────────────────────────────────────────────────────────────────────
  describe("ո word-initial context rules", () => {
    test("ո isolated → во", () => {
      expect(t("\u0578")).toBe("\u0432\u043E");
    });

    test("Ո isolated → ВО", () => {
      expect(t("\u0548")).toBe("\u0412\u041E");
    });

    test("ո+ն (word-initial + consonant) → вон", () => {
      expect(t("\u0578\u0576")).toBe("\u0432\u043E\u043D");
    });

    test("ո+վ (ո followed by վ) → ов, not во", () => {
      expect(t("\u0578\u057E")).toBe("\u043E\u0432");
    });

    test("ո+ա (ո followed by vowel) → оа, not во", () => {
      expect(t("\u0578\u0561")).toBe("\u043E\u0430");
    });

    test("բ+ο+բ (mid-word ο) → боб, not бвоб", () => {
      expect(t(mid("\u0578"))).toBe("\u0431\u043E\u0431");
    });

    test("Ոski (Oski) → Воски", () => {
      expect(t("\u0548\u057D\u056F\u056B")).toBe(
        "\u0412\u043E\u0441\u043A\u0438",
      );
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // ղ positional: word-initial → к; mid-word → г
  // ─────────────────────────────────────────────────────────────────────────
  describe("ղ positional rule: word-initial к, mid-word г", () => {
    test("ղ isolated → к (word-initial)", () => {
      expect(t("\u0572")).toBe("\u043A");
    });

    test("Ղ isolated → К", () => {
      expect(t("\u0542")).toBe("\u041A");
    });

    test("ղ+ա+ռ+ա+բ+ա+ղ (Karabakh) → карабах (initial к, final г)", () => {
      expect(t("\u0572\u0561\u057C\u0561\u0562\u0561\u056D")).toBe(
        "\u043A\u0430\u0440\u0430\u0431\u0430\u0445",
      );
    });

    test("բ+ղ+բ (mid-word ղ) → бгб (not бкб)", () => {
      expect(t(mid("\u0572"))).toBe("\u0431\u0433\u0431");
    });

    test("Մeghri: Մ+ե+ղ+ր+ի → Мегри (ղ mid-word → г)", () => {
      expect(t("\u0544\u0565\u0572\u0580\u056B")).toBe(
        "\u041C\u0435\u0433\u0440\u0438",
      );
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // ջ → дж (KEY difference from old russian-phonetic)
  // ─────────────────────────────────────────────────────────────────────────
  describe("ջ → дж", () => {
    test("ջ isolated → дж", () => {
      expect(t("\u057B")).toBe("\u0434\u0436");
    });

    test("Ջ isolated → ДЖ", () => {
      expect(t("\u054B")).toBe("\u0414\u0416");
    });

    test("title: Ջejkun → Джейкун", () => {
      expect(t("\u054B\u0565\u0575\u056F\u0578\u0582\u0576")).toBe(
        "\u0414\u0436\u0435\u0439\u043A\u0443\u043D",
      );
    });

    test("ā+ջ+ā: aghjik (ա+ղ+ջ+ι+κ) → агджик (not агжик)", () => {
      expect(t("\u0561\u0572\u057B\u056B\u056F")).toBe(
        "\u0430\u0433\u0434\u0436\u0438\u043A",
      );
    });

    test("ジ mid-word: բ+ջ+բ → бджб", () => {
      expect(t(mid("\u057B"))).toBe("\u0431\u0434\u0436\u0431");
    });

    test("ジ ≠ ژ: ժ isolated → ж (distinct from ջ → дж)", () => {
      expect(t("\u056A")).toBe("\u0436");
      expect(t("\u057B")).toBe("\u0434\u0436");
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // Sequences: mouth (ու), յou (յու), ya (յа), ev (and/еւ)
  // ─────────────────────────────────────────────────────────────────────────
  describe("sequence ου (ու) → у", () => {
    test("ου isolated → у", () => {
      expect(t("\u0578\u0582")).toBe("\u0443");
    });

    test("Ου standalone → У", () => {
      expect(t("\u0548\u0582")).toBe("\u0423");
    });

    test("Urevan: Ο+υ+ρ+ε+β+α+ν → Уреван", () => {
      expect(t("\u0548\u0582\u0580\u0565\u057E\u0561\u0576")).toBe(
        "\u0423\u0440\u0435\u0432\u0430\u043D",
      );
    });

    test("mid-word ου: բ+ου+բ → буб", () => {
      expect(t(`${B}\u0578\u0582${B}`)).toBe("\u0431\u0443\u0431");
    });
  });

  describe("sequence υου (յou) → ю", () => {
    test("յou isolated → ю", () => {
      expect(t("\u0575\u0578\u0582")).toBe("\u044E");
    });

    test("Հaнрапетутюн: Հ+ա+ν+ρ+α+π+ε+τ+ου+τ+υου+ν → Ханрапетутюн", () => {
      expect(
        t(
          "\u0540\u0561\u0576\u0580\u0561\u057A\u0565\u057F\u0578\u0582\u0569\u0575\u0578\u0582\u0576",
        ),
      ).toBe(
        "\u0425\u0430\u043D\u0440\u0430\u043F\u0435\u0442\u0443\u0442\u044E\u043D",
      );
    });
  });

  describe("sequence υα (յa) → я", () => {
    test("Հayastan: Հ+ա+υα+α+ς+τ+α+ν → Хаяcтан", () => {
      expect(t("\u0540\u0561\u0575\u0561\u057D\u057F\u0561\u0576")).toBe(
        "\u0425\u0430\u044F\u0441\u0442\u0430\u043D",
      );
    });

    test("Petrosyan: Π+ε+τ+ρ+ο+ς+υα+ν → Петросян", () => {
      expect(t("\u054A\u0565\u057F\u0580\u0578\u057D\u0575\u0561\u0576")).toBe(
        "\u041F\u0435\u0442\u0440\u043E\u0441\u044F\u043D",
      );
    });
  });

  describe("sequence and (և U+0587) → ев", () => {
    test("and isolated → ев", () => {
      expect(t("\u0587")).toBe("\u0435\u0432");
    });

    test("Ереван: Ε+ρ+and+α+ν → Ереван", () => {
      expect(t("\u0535\u057C\u0587\u0561\u0576")).toBe(
        "\u0415\u0440\u0435\u0432\u0430\u043D",
      );
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // Full Armenian word transliterations
  // ─────────────────────────────────────────────────────────────────────────
  describe("full Armenian word transliterations", () => {
    test.each([
      // Geographic names
      [
        "\u0540\u0561\u0575\u0561\u057D\u057F\u0561\u0576",
        "\u0425\u0430\u044F\u0441\u0442\u0430\u043D",
      ], // Հaяcтан → Хаяcтан
      [
        "\u053F\u0565\u0576\u057F\u0580\u0578\u0576",
        "\u041A\u0435\u043D\u0442\u0440\u043E\u043D",
      ], // Кентрон
      [
        "\u0535\u057C\u0587\u0561\u0576",
        "\u0415\u0440\u0435\u0432\u0430\u043D",
      ], // Ереван
      [
        "\u054F\u056B\u0563\u0580\u0561\u0576",
        "\u0422\u0438\u0433\u0440\u0430\u043D",
      ], // Тигран
      ["\u0547\u0578\u0572\u0565\u0580", "\u0428\u043E\u0433\u0435\u0440"], // Шогер
      ["\u0548\u057D\u056F\u056B", "\u0412\u043E\u0441\u043A\u0438"], // Воски
      [
        "\u0531\u0580\u0574\u0565\u0576\u056B\u0561",
        "\u0410\u0440\u043C\u0435\u043D\u0438\u0430",
      ], // Армениа
      // Words with ջ → дж (changed)
      [
        "\u0561\u0572\u057B\u056B\u056F",
        "\u0430\u0433\u0434\u0436\u0438\u043A",
      ], // aghjik → агджик (not агжик)
      [
        "\u0548\u0572\u057B\u0578\u0582\u0575\u0576",
        "\u0412\u043E\u0433\u0434\u0436\u0443\u0439\u043D",
      ], // Ooghjuyn → Вогджуйн
      // Common words
      ["\u056B\u0576\u0584\u0568", "\u0438\u043D\u043A\u044B"], // ինqը → инкы
      ["\u056F\u0561\u0580\u0578\u0572", "\u043A\u0430\u0440\u043E\u0433"], // կarəɤ → карог
      ["\u0576\u0561\u0587", "\u043D\u0430\u0435\u0432"], // naew → наев
      ["\u0574\u0565\u057B", "\u043C\u0435\u0434\u0436"], // mej → медж (not меж!)
      ["\u0569\u0565", "\u0442\u0435"], // թe → те
      ["\u0578\u0579", "\u0432\u043E\u0447"], // вочь → воч
      ["\u0565\u057D", "\u0435\u0441"], // yes → ес
    ])('"%s" → "%s"', (armenian, expected) => {
      expect(t(armenian)).toBe(expected);
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // Multi-word phrases
  // ─────────────────────────────────────────────────────────────────────────
  describe("multi-word phrase transliterations", () => {
    test("Nor u Norits", () => {
      expect(
        t("\u0546\u0578\u0580 \u0578\u0582 \u0576\u0578\u0580\u056B\u0581"),
      ).toBe("\u041D\u043E\u0440 \u0443 \u043D\u043E\u0440\u0438\u0446");
    });

    test("Voski Or → Воски Ор", () => {
      expect(t("\u0548\u057D\u056F\u056B \u0555\u0580")).toBe(
        "\u0412\u043E\u0441\u043A\u0438 \u041E\u0440",
      );
    });

    test("Khagh chem gnum restoran", () => {
      expect(
        t(
          "\u053D\u0561\u0572 \u0579\u0565\u0574 \u0563\u0576\u0578\u0582\u0574 \u057C\u0565\u057D\u057F\u0578\u0580\u0561\u0576",
        ),
      ).toBe(
        "\u0425\u0430\u0433 \u0447\u0435\u043C \u0433\u043D\u0443\u043C \u0440\u0435\u0441\u0442\u043E\u0440\u0430\u043D",
      );
    });
  });
});
