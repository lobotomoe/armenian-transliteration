/**
 * Russian Phonetic standard — comprehensive character mapping tests.
 *
 * All expected values were verified against the live transliteration engine.
 *
 * Key differences from BGN/PCGN:
 *   - ε (U+0565) word-initial → "е" (same Cyrillic е; no special prefix)
 *   - ο (U+0578) word-initial + notFollowedBy[vowels/β] → "во"
 *   - Different consonant mappings: ч→ж, χ→χ, ψ→г, ρ→дз, σ→ч, etc.
 *   - Sequences: υου→ю, υα→я, ου→у, and→ев, traditional εω→ев
 *
 * Armenian codepoints used (same as bgn-pcgn.test.ts — see that file for reference).
 */
import { transliterate } from "../../src";

const t = (text: string) => transliterate(text, { standard: "russian-phonetic" });

const B = "\u0562"; // բ — neutral mid-word wrapper
const mid = (ch: string) => `${B}${ch}${B}`;

describe("Russian Phonetic standard", () => {
  // ─────────────────────────────────────────────────────────────────────────
  // All 38 lowercase letters in mid-word context
  // ─────────────────────────────────────────────────────────────────────────
  describe("38 lowercase letter mappings (mid-word context)", () => {
    test.each([
      ["\u0561", "\u0430"],   // ա → а
      ["\u0562", "\u0431"],   // բ → б
      ["\u0563", "\u0433"],   // գ → г
      ["\u0564", "\u0434"],   // դ → д
      ["\u0565", "\u0435"],   // ե → е (mid-word; word-initial also е in Russian)
      ["\u0566", "\u0437"],   // զ → з
      ["\u0567", "\u044D"],   // է → э
      ["\u0568", "\u044B"],   // ը → ы
      ["\u0569", "\u0442"],   // թ → т
      ["\u056A", "\u0436"],   // ժ → ж
      ["\u056B", "\u0438"],   // ի → и
      ["\u056C", "\u043B"],   // լ → л
      ["\u056D", "\u0445"],   // խ → х
      ["\u056E", "\u0446"],   // ծ → ц
      ["\u056F", "\u043A"],   // կ → к
      ["\u0570", "\u0445"],   // հ → х
      ["\u0571", "\u0434\u0437"], // ձ → дз
      ["\u0572", "\u0433"],   // ղ → г
      ["\u0573", "\u0447"],   // ճ → ч
      ["\u0574", "\u043C"],   // մ → м
      ["\u0575", "\u0439"],   // յ → й
      ["\u0576", "\u043D"],   // ն → н
      ["\u0577", "\u0448"],   // շ → ш
      ["\u0578", "\u043E"],   // ο → о (mid-word; no во)
      ["\u0579", "\u0447"],   // չ → ч
      ["\u057A", "\u043F"],   // պ → п
      ["\u057B", "\u0436"],   // ջ → ж
      ["\u057C", "\u0440"],   // ռ → р
      ["\u057D", "\u0441"],   // ς → с
      ["\u057E", "\u0432"],   // β → в
      ["\u057F", "\u0442"],   // τ → т
      ["\u0580", "\u0440"],   // ρ → р
      ["\u0581", "\u0446"],   // ց → ц
      ["\u0582", "\u0443"],   // ω → у
      ["\u0583", "\u043F"],   // փ → п
      ["\u0584", "\u043A"],   // ք → к
      ["\u0585", "\u043E"],   // օ → о
      ["\u0586", "\u0444"],   // ֆ → ф
    ])("\u0562%s\u0562 → \u0431%s\u0431", (ch, expected) => {
      expect(t(mid(ch))).toBe(`\u0431${expected}\u0431`);
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // All 38 uppercase single-letter words
  // Ε (U+0535) isolated → context rule fires but maps to "е" → all-caps → "Е"
  // Ο (U+0548) isolated → "во" → all-caps → "ВО"
  // ─────────────────────────────────────────────────────────────────────────
  describe("38 uppercase single-letter words", () => {
    test.each([
      ["\u0531", "\u0410"],         // Ա → А
      ["\u0532", "\u0411"],         // Բ → Б
      ["\u0533", "\u0413"],         // Γ → Г
      ["\u0534", "\u0414"],         // Δ → Д
      ["\u0535", "\u0415"],         // Ε — word-initial "е" → Е (no special prefix in Russian)
      ["\u0536", "\u0417"],         // Ζ → З
      ["\u0537", "\u042D"],         // Η → Э
      ["\u0538", "\u042B"],         // Θ → Ы
      ["\u0539", "\u0422"],         // Θ → Т
      ["\u053A", "\u0416"],         // Ι → Ж
      ["\u053B", "\u0418"],         // Ι → И
      ["\u053C", "\u041B"],         // Κ → Л
      ["\u053D", "\u0425"],         // Λ → Х
      ["\u053E", "\u0426"],         // Μ → Ц
      ["\u053F", "\u041A"],         // Ν → К
      ["\u0540", "\u0425"],         // Ξ → Х
      ["\u0541", "\u0414\u0417"],   // Ο → ДЗ
      ["\u0542", "\u0413"],         // Π → Г
      ["\u0543", "\u0427"],         // Ρ → Ч
      ["\u0544", "\u041C"],         // Σ → М
      ["\u0545", "\u0419"],         // Τ → Й
      ["\u0546", "\u041D"],         // Υ → Н
      ["\u0547", "\u0428"],         // Φ → Ш
      ["\u0548", "\u0412\u041E"],   // Ο — isolated, nothing follows → ВО
      ["\u0549", "\u0427"],         // Χ → Ч
      ["\u054A", "\u041F"],         // Ψ → П
      ["\u054B", "\u0416"],         // Ω → Ж
      ["\u054C", "\u0420"],         // Ρ → Р
      ["\u054D", "\u0421"],         // Σ → С
      ["\u054E", "\u0412"],         // Β → В
      ["\u054F", "\u0422"],         // Τ → Т
      ["\u0550", "\u0420"],         // Υ → Р
      ["\u0551", "\u0426"],         // Ω → Ц
      ["\u0552", "\u0423"],         // Ω → У
      ["\u0553", "\u041F"],         // Φ → П
      ["\u0554", "\u041A"],         // Ψ → К
      ["\u0555", "\u041E"],         // Ω → О
      ["\u0556", "\u0424"],         // Φ → Ф
    ])("%s → %s", (armenian, expected) => {
      expect(t(armenian)).toBe(expected);
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // Context rule: ε (U+0565) word-initial → "е" (same letter — Russian keeps е)
  // The context rule exists but maps to the same "е", so no visible change.
  // ─────────────────────────────────────────────────────────────────────────
  describe("ε (U+0565) word-initial context (same as mid-word in Russian)", () => {
    test("ε (isolated) → е", () => {
      expect(t("\u0565")).toBe("\u0435");
    });

    test("Ε (isolated uppercase) → Е (all-caps: Е)", () => {
      expect(t("\u0535")).toBe("\u0415");
    });

    test("β+ε+β (mid-word ε) → бeб", () => {
      expect(t(mid("\u0565"))).toBe("\u0431\u0435\u0431");
    });

    test("Ε+ρ+ε+β+α+ν (Yerevan) → Ереван", () => {
      expect(t("\u0535\u057C\u0587\u0561\u0576")).toBe("\u0415\u0440\u0435\u0432\u0430\u043D");
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // Context rule: ο (U+0578) word-initial + notFollowedBy[vowels/β] → "во"
  // VOWELS for Russian: [ա U+0561, ε U+0565, ε U+0567, ε U+0568, ι U+056B,
  //                      ο U+0578, ο U+0585] + β U+057E
  // ─────────────────────────────────────────────────────────────────────────
  describe("ο (U+0578) word-initial context rules", () => {
    test("ο (isolated) → во", () => {
      expect(t("\u0578")).toBe("\u0432\u043E");
    });

    test("Ο (isolated) → ВО", () => {
      expect(t("\u0548")).toBe("\u0412\u041E");
    });

    test("ο+ν (word-initial + consonant) → вон", () => {
      expect(t("\u0578\u0576")).toBe("\u0432\u043E\u043D");
    });

    test("ο+χ+ι (word-initial + χ) → вочи", () => {
      expect(t("\u0578\u0579\u056B")).toBe("\u0432\u043E\u0447\u0438");
    });

    test("ο+β (ο followed by β — in VOWELS list) → ов, not во", () => {
      expect(t("\u0578\u057E")).toBe("\u043E\u0432");
    });

    test("ο+α (ο followed by α — vowel) → оа, not во", () => {
      expect(t("\u0578\u0561")).toBe("\u043E\u0430");
    });

    test("ο+ο (ο followed by ο — vowel) → оо, not во", () => {
      expect(t("\u0578\u0578")).toBe("\u043E\u043E");
    });

    test("ο+ι (ο followed by ι — vowel) → ои, not во", () => {
      expect(t("\u0578\u056B")).toBe("\u043E\u0438");
    });

    test("β+ο+β (mid-word ο) → боб, not бвоб", () => {
      expect(t(mid("\u0578"))).toBe("\u0431\u043E\u0431");
    });

    test("Ο+ν (Ο word-initial + consonant) → Вон", () => {
      expect(t("\u0548\u0576")).toBe("\u0412\u043E\u043D");
    });

    test("Ο+β (Ο followed by β) → Ов, not Во", () => {
      expect(t("\u0548\u057E")).toBe("\u041E\u0432");
    });

    test("Ο+ς+κ+ι (Oski) → Воски", () => {
      expect(t("\u0548\u057D\u056F\u056B")).toBe("\u0412\u043E\u0441\u043A\u0438");
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // Sequence: ου (U+0578 + U+0582) → "у"
  // ─────────────────────────────────────────────────────────────────────────
  describe("sequence ου (U+0578+U+0582) → у", () => {
    test("ου (isolated) → у", () => {
      expect(t("\u0578\u0582")).toBe("\u0443");
    });

    test("Ου standalone → У", () => {
      expect(t("\u0548\u0582")).toBe("\u0423");
    });

    test("ο+υ+ρ+α+χ (Urakh) → Урах", () => {
      expect(t("\u0548\u0582\u0580\u0561\u056D")).toBe("\u0423\u0440\u0430\u0445");
    });

    test("β+ου+β (mid-word) → буб", () => {
      expect(t(`${B}\u0578\u0582${B}`)).toBe("\u0431\u0443\u0431");
    });

    test("ΤΙΜΟΥΡ all-caps → ТИМУР", () => {
      expect(t("\u054F\u053B\u0544\u0548\u0582\u0550")).toBe("\u0422\u0418\u041C\u0423\u0420");
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // Sequence: υου (U+0575 + U+0578 + U+0582) → "ю"
  // ─────────────────────────────────────────────────────────────────────────
  describe("sequence υου (U+0575+U+0578+U+0582) → ю", () => {
    test("υου (isolated) → ю", () => {
      expect(t("\u0575\u0578\u0582")).toBe("\u044E");
    });

    test("β+υου+β (mid-word) → бюб", () => {
      expect(t(`${B}\u0575\u0578\u0582${B}`)).toBe("\u0431\u044E\u0431");
    });

    test("Η+α+ν+ρ+α+π+ε+τ+υου+τ+υου+ν → Ханрапетутюн", () => {
      // Hanrapetutyun: Η(U+0540)+α(U+0561)+ν(U+0576)+ρ(U+0580)+α(U+0561)+π(U+057A)+ε(U+0565)+τ(U+057F)+υου+τ(U+057F)+υου+ν(U+0576)
      expect(t("\u0540\u0561\u0576\u0580\u0561\u057A\u0565\u057F\u0578\u0582\u0569\u0575\u0578\u0582\u0576")).toBe("\u0425\u0430\u043D\u0440\u0430\u043F\u0435\u0442\u0443\u0442\u044E\u043D");
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // Sequence: υα (U+0575 + U+0561) → "я"
  // ─────────────────────────────────────────────────────────────────────────
  describe("sequence υα (U+0575+U+0561) → я", () => {
    test("υα (isolated) → я", () => {
      expect(t("\u0575\u0561")).toBe("\u044F");
    });

    test("β+υα+β (mid-word) → бяб", () => {
      expect(t(`${B}\u0575\u0561${B}`)).toBe("\u0431\u044F\u0431");
    });

    test("Η+α+υα+α+ς+τ+α+ν (Hayastan) → Хаястан", () => {
      expect(t("\u0540\u0561\u0575\u0561\u057D\u057F\u0561\u0576")).toBe("\u0425\u0430\u044F\u0441\u0442\u0430\u043D");
    });

    test("Πετρος+υα+ν (Petrosyan) → Петросян", () => {
      // Π(U+054A)+ε(U+0565)+τ(U+057F)+ρ(U+0580)+ο(U+0578)+ς(U+057D)+υα+ν(U+0576)
      expect(t("\u054A\u0565\u057F\u0580\u0578\u057D\u0575\u0561\u0576")).toBe("\u041F\u0435\u0442\u0440\u043E\u0441\u044F\u043D");
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // Sequence: and ligature (U+0587) → "ев" (no word-initial prefix in Russian)
  // ─────────────────────────────────────────────────────────────────────────
  describe("sequence and (U+0587) → ев (always, no yev variant)", () => {
    test("and (isolated) → ев", () => {
      expect(t("\u0587")).toBe("\u0435\u0432");
    });

    test("β+and+β (mid-word) → бевб", () => {
      expect(t(`${B}\u0587${B}`)).toBe("\u0431\u0435\u0432\u0431");
    });

    test("Μεζ and Δζεζ (standalone) → Мез ев Дзез", () => {
      expect(t("\u0544\u0565\u0566 \u0587 \u0534\u0566\u0565\u0566")).toBe("\u041C\u0435\u0437 \u0435\u0432 \u0414\u0437\u0435\u0437");
    });

    test("δ+ε+ρ+and+ς → деревс", () => {
      expect(t("\u0564\u0565\u057C\u0587\u057D")).toBe("\u0434\u0435\u0440\u0435\u0432\u0441");
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // Sequence: traditional εω (U+0565 + U+0582) → "ев"
  // ─────────────────────────────────────────────────────────────────────────
  describe("sequence εω traditional (U+0565+U+0582) → ев", () => {
    test("εω (isolated) → ев", () => {
      expect(t("\u0565\u0582")).toBe("\u0435\u0432");
    });

    test("β+εω+β (mid-word) → бевб", () => {
      expect(t(`${B}\u0565\u0582${B}`)).toBe("\u0431\u0435\u0432\u0431");
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // Full Armenian word transliterations
  // ─────────────────────────────────────────────────────────────────────────
  describe("full Armenian word transliterations", () => {
    test.each([
      ["\u0540\u0561\u0575\u0561\u057D\u057F\u0561\u0576", "\u0425\u0430\u044F\u0441\u0442\u0430\u043D"],   // Հayastan → Хаястан
      ["\u053F\u0565\u0576\u057F\u0580\u0578\u0576", "\u041A\u0435\u043D\u0442\u0440\u043E\u043D"],          // Κentron → Кентрон
      ["\u0535\u057C\u0587\u0561\u0576", "\u0415\u0440\u0435\u0432\u0430\u043D"],                            // Ε+ρ+and+α+ν → Ереван
      ["\u0548\u0572\u057B\u0578\u0582\u0575\u0576", "\u0412\u043E\u0433\u0436\u0443\u0439\u043D"],          // Ooghjuyn → Вогжуйн
      ["\u054F\u056B\u0563\u0580\u0561\u0576", "\u0422\u0438\u0433\u0440\u0430\u043D"],                      // Τigran → Тигран
      ["\u0547\u0578\u0572\u0565\u0580", "\u0428\u043E\u0433\u0435\u0440"],                                   // Shogher → Шогер
      ["\u0548\u057D\u056F\u056B", "\u0412\u043E\u0441\u043A\u0438"],                                        // Oski → Воски
      ["\u0561\u0572\u057B\u056B\u056F", "\u0430\u0433\u0436\u0438\u043A"],                                  // aghjik → агжик
      ["\u0544\u0565\u0572\u0565\u0564\u056B", "\u041C\u0435\u0433\u0435\u0434\u0438"],                      // Meghedi → Мегеди
      ["\u0531\u0580\u0574\u0565\u0576\u056B\u0561", "\u0410\u0440\u043C\u0435\u043D\u0438\u0430"],          // Armenia → Армениа
    ])('"%s" → "%s"', (armenian, expected) => {
      expect(t(armenian)).toBe(expected);
    });
  });

  // ─────────────────────────────────────────────────────────────────────────
  // Multi-word phrase transliterations
  // ─────────────────────────────────────────────────────────────────────────
  describe("multi-word phrase transliterations", () => {
    test("Nor u norits", () => {
      expect(t("\u0546\u0578\u0580 \u0578\u0582 \u0576\u0578\u0580\u056B\u0581")).toBe("\u041D\u043E\u0440 \u0443 \u043D\u043E\u0440\u0438\u0446");
    });

    test("Voski Or → Воски Ор", () => {
      expect(t("\u0548\u057D\u056F\u056B \u0555\u0580")).toBe("\u0412\u043E\u0441\u043A\u0438 \u041E\u0440");
    });

    test("Mez ев Dzez", () => {
      expect(t("\u0544\u0565\u0566 \u0587 \u0534\u0566\u0565\u0566")).toBe("\u041C\u0435\u0437 \u0435\u0432 \u0414\u0437\u0435\u0437");
    });

    test("Khagh chem gnum restoran", () => {
      expect(t("\u053D\u0561\u0572 \u0579\u0565\u0574 \u0563\u0576\u0578\u0582\u0574 \u057C\u0565\u057D\u057F\u0578\u0580\u0561\u0576")).toBe("\u0425\u0430\u0433 \u0447\u0435\u043C \u0433\u043D\u0443\u043C \u0440\u0435\u0441\u0442\u043E\u0440\u0430\u043D");
    });
  });
});
